//Methods with Parameters

class PaintInfo
{
	public static void origin(String brand)
	{
		System.out.println("Brand name of the paint is: "+brand);
	}
	public static void colorInfo(String brand,String color)
	{
		System.out.println("Brand name of the paint is: "+brand);
		System.out.println("color of the paint: "+color);
	}
	public static void retailInfo(String brand,double price)
	{
		System.out.println("Brand name of the paint is: "+brand);
		System.out.println("price of the paint: "+price);
	}
	public static void info(String color,String type,double price)
	{
		System.out.println("color of the paint: "+color);
		System.out.println("Type of the paint: "+type);
		System.out.println("price of the paint is: "+price);
	}
}
	
	
	